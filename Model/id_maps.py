def remap(lut, arr):
    """Remap vettoriale con clip: ID fuori tabella -> 0."""
    a = np.asarray(arr, dtype=np.int64)
    out = np.where((a >= 0) & (a < len(lut)), lut[np.clip(a, 0, len(lut) - 1)], 0)
    return out
